    public void LoadQuestion(){

      
        Question aa= a.questions.get(qno);
        
        this.jLabel3.setText(aa.questionText);
        this.jRadioButton1.setText(aa.options[0]);
        this.jRadioButton2.setText(aa.options[1]);
        this.jRadioButton3.setText(aa.options[2]);
        this.jRadioButton4.setText(aa.options[3]);
        c2=aa.correctOption;
           if (task != null) {
            task.cancel(); 
        }
       task = new TimerTask() {
            @Override
            public void run() {
                tickCount--;
                ti.setText(String.valueOf(tickCount));
                if (ti.getText().contentEquals("0")) {
                      submitAnswer();
                }
            }
        };

        timer.scheduleAtFixedRate(task, 0, 1000);
    
    }
    
