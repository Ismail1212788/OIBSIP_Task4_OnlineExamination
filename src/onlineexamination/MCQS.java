    private void submitAnswer(){
        String data=this.jLabel6.getText();
        int a2=Integer.parseInt(data);
       
        if(c1==c2){
             a2+=5;
            this.jLabel6.setText(String.valueOf(a2));
        }

          qno++;
      
         if (qno < a.getQuestionSize()) {
                LoadQuestion();
            tickCount = 30;
                } 
         if(qno==a.getQuestionSize()){
             JOptionPane.showMessageDialog(
            null,                   
            "Your Quiz has been completed\nYour Total Score is : "+String.valueOf(a2), 
            "Done",          // Dialog title
            JOptionPane.INFORMATION_MESSAGE
        );
           this.dispose();
         }
    }
    
    
